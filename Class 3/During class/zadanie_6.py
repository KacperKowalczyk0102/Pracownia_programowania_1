# 6. Ograniczenie prędkości na autostradzie to 130 km/h.
# Napisz program sprawdzający, czy samochód przekroczył dozwoloną prędkość.

speed_limit = 130
car_speed = int(input("Podaj prędkość zamochodu: "))

if car_speed > speed_limit:
    print("Przekroczył prędkość !")
else:
    print("Nie przekroczył mimitu")
    


